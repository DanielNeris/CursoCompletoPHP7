<?php

    $a = 30;
    $b = 55;

    //maior
    var_dump($a > $b);
    echo '<br>';

    //menor
    var_dump($a < $b);
    echo '<br>';

    //maior-igual
    var_dump($a >= $b);
    echo '<br>';

    //menor-igual
    var_dump($a <= $b);
    echo '<br>';

    //atribução
    var_dump($a = $b);
    echo '<br>';

    //comparção (valor)
    var_dump('1' == 1);
    echo '<br>';

    //comparção (valor e tipo)
    var_dump('1' === 1);
    echo '<br>';

    //diferente (valor)
    var_dump('1' != 1);
    echo '<br>';

    //diferente (valor e  tipo)
    var_dump('1' !== 1);
    echo '<br>';

?>